# My VSCode Theme
#--------------------------------------------------------------------------------------------------------------------------------------------
# Background
set -g status-bg "#1E1E1E"
# Session name
set -g status-left "#[fg=#1E1E1E,bg=#608B4E,bold] #S "
# Focus
set -g window-status-current-format "#[fg=brightwhite,bg=black,bold]   #I #W "
# Unfocused
set -g window-status-format "#[fg=white,bg=default,nobold,noitalics,nounderscore]   #I #W "
# Right
set -g status-right "#[fg=#1E1E1E,bg=#608B4E,bold] %Y-%m-%d ❬ %H:%M:%S "
# Text between windows
set -g window-status-separator "" 
